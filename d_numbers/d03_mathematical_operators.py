print('Addition')
print(100 + 42)
print('---')

print('Substraction')
print(100 - 42)
print('---')

print('Division')
print(100 / 42)   # 2.380952380952381
print(100 / 38)   # 2.6315789473684212
print('---')

print('Floor Division')
print(100 // 42)  # 2
print(100 // 38)  # 2
print('---')

print('Multiplication')
print(100 * 42)
print('---')

print('Modulus')
print(2 % 2)  # 0 -> even number
print(25 % 2)  # 1 -> odd number
print('---')

print('Exponents')
print(11 ** 2)
print('---')


